



public class Primitivos {

	public static void main(String[] args) {
		//Tipos primitivos (variáveis)
		//tipo booleano (verdadeiro ou falso)
		boolean completo = true;
		//tipo char (1 caractere unicode 'aspas simples')
		char sexo = 'F';
		//tipo byte (números inteiros)
		byte idade= 18;
		//tipo short (numeros inteiros)
		short numero1 = 16780;
		int numero2 = 1968; // padrao para numero inteiros
		//tipo int long (numeros inteiros) ***L indica que é numero grande
		
		long numero3= 2147836478L;
		// tipo float (numero nao inteiro) se trabalhar com
		float nota1 = 5.6f;
		//tipo double (numero nao inteiro)
		double nota2 = 7.8; //padrao do java para numeros nao inteiros
		String nome= " Sirlene";
		System.out.println(completo);
		System.out.println(sexo);
        System.out.println(idade);
        System.out.println(numero1);
        System.out.println(numero2);
        System.out.println(numero3);
        System.out.println(nota1);
        System.out.println(nota2);
        System.out.println(nome);
        
	}

}
